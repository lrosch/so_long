#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	res;
	int	vrz;

	i = 0;
	vrz = 1;
	res = 0;
	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			vrz = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + str[i] - '0';
		i++;
	}
	return (vrz * res);
}
